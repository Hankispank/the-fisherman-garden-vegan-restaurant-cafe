window.I18N = (function () {
  const STORAGE_KEY = "gl_lang";
  let current =
    localStorage.getItem(STORAGE_KEY) ||
    (navigator.language || "en").slice(0, 2).toLowerCase();
  if (!window.TRANSLATIONS[current]) current = "en";

  function t(key) {
    const dict = window.TRANSLATIONS[current] || window.TRANSLATIONS.en;
    return dict[key] ?? window.TRANSLATIONS.en[key] ?? key;
  }

  function apply() {
    document.documentElement.lang = current;

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
      el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
    });

    const titleEl = document.querySelector("title[data-i18n]");
    if (titleEl) document.title = t(titleEl.getAttribute("data-i18n"));
    const desc = document.querySelector('meta[name="description"]');
    if (desc) desc.setAttribute("content", t("meta.description"));

    document.querySelectorAll(".lang__btn").forEach((b) =>
      b.classList.toggle("is-active", b.dataset.lang === current)
    );

    document.dispatchEvent(new CustomEvent("languagechange", { detail: current }));
  }

  function set(lang) {
    if (!window.TRANSLATIONS[lang]) return;
    current = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    apply();
  }

  return {
    t,
    apply,
    set,
    get lang() {
      return current;
    },
  };
})();
